function showinput() {
    var inputsearch =document.querySelector('.s-input')
    // inputsearch.className = 's-inputhover'
    inputsearch.style.display = 'block'
    inputsearch.style.width = '300px'
    
       

}


function darkmode() {
    var inputsearch =document.querySelector('body')
    // inputsearch.className = 's-inputhover'
    inputsearch.style.backgroundColor = 'black'
    inputsearch.style.color = 'white'
    
       

}

function lightmode() {
    var inputsearch =document.querySelector('body')
    // inputsearch.className = 's-inputhover'
    inputsearch.style.backgroundColor = 'white'
    inputsearch.style.color = 'black'
    
       

}